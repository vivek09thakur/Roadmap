// Type Casting or Type Conversion

let score = "33ab";
console.log("CASE 1 :", typeof score, `Data = ${score}`);

let valInNum = Number(score);
console.log("CASE 2 :", typeof valInNum, `Data = ${valInNum}`);

let isLoggedIn = 0;
let boolisLoggedIn = Boolean(isLoggedIn);
console.log("CASE 3 :", typeof boolisLoggedIn, `Data = ${boolisLoggedIn}`);
